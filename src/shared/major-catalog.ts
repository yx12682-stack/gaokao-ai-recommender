function guideToProfile(major: BaseMajor, guide: Guide): MajorProfile {
  return {
    plainLanguage: guide.plainLanguage(major),
    interests: guide.interests,
    suitableFor: guide.suitableFor,
    coreCourses: guide.coreCourses,
    employmentDirections: guide.employmentDirections,
    representativeCareers: guide.representativeCareers,
    industryOutlook: guide.industryOutlook,
    graduateDirections: guide.graduateDirections,
    riskReminder: guide.riskReminder
  };
}

function profileFor(major: BaseMajor): MajorProfile {
  const featured = featuredProfiles[major.name];
  if (featured) return featured;

  const classGuide = classGuides.find((item) =>
    item.matches.some(
      (keyword) =>
        major.name.includes(keyword) || major.majorClass.includes(keyword) || major.category.includes(keyword)
    )
  );
  if (classGuide) return guideToProfile(major, classGuide.guide);

  return guideToProfile(major, categoryGuides[major.category] ?? defaultGuide);
}

function relatedMajorsFor(major: BaseMajor) {
  const sameClass = majorRows
    .filter((item) => item.name !== major.name && item.majorClass === major.majorClass)
    .map((item) => item.name)
    .slice(0, 5);
  if (sameClass.length >= 3) return sameClass;

  const sameCategory = majorRows
    .filter(
      (item) => item.name !== major.name && item.category === major.category && !sameClass.includes(item.name)
    )
    .map((item) => item.name)
    .slice(0, 5 - sameClass.length);

  return [...sameClass, ...sameCategory];
}

export const nationalMajorCatalog: MajorCatalogEntry[] = majorRows.map((major) => ({
  code: major.code,
  name: major.name,
  category: major.category,
  majorClass: major.majorClass,
  degreeCategory: major.degreeCategory,
  ...profileFor(major),
  relatedMajors: relatedMajorsFor(major),
  dataSource: ministryMajorCatalogSource
}));

function normalizeSearchText(value: string) {
  return value.trim().toLowerCase();
}

function searchScore(entry: MajorCatalogEntry, query: string) {
  const name = normalizeSearchText(entry.name);
  const code = normalizeSearchText(entry.code);
  const majorClass = normalizeSearchText(entry.majorClass);
  const category = normalizeSearchText(entry.category);
  const plainLanguage = normalizeSearchText(entry.plainLanguage);

  if (name === query) return 1000;
  if (code === query) return 950;
  if (name.startsWith(query)) return 900;
  if (name.includes(query)) return 800;
  if (majorClass === query) return 700;
  if (majorClass.startsWith(query)) return 650;
  if (category === query) return 600;
  if (code.includes(query)) return 550;
  if (majorClass.includes(query)) return 500;
  if (category.includes(query)) return 450;
  if (plainLanguage.includes(query)) return 100;

  return 0;
}

export function getMajorCatalog(query = ""): MajorCatalogEntry[] {
  const normalizedQuery = normalizeSearchText(query);
  if (!normalizedQuery) return nationalMajorCatalog;

  return nationalMajorCatalog
    .map((entry, index) => ({
      entry,
      index,
      score: searchScore(entry, normalizedQuery)
    }))
    .filter((item) => item.score > 0)
    .sort((left, right) => right.score - left.score || left.index - right.index)
    .map((item) => item.entry);
}

export function getMajorCatalogEntry(name: string): MajorCatalogEntry | undefined {
  const cleanName = name.trim().replace(/（注：.*$/, "");
  return nationalMajorCatalog.find((entry) => entry.name === cleanName);
}
